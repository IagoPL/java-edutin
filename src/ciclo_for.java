public class ciclo_for {
    public static void main(String[] args) {
        
        int limite = 50;

        int contador;

        System.out.println("cuenta creciente");

        for(contador=0; contador  < limite; contador++)
        {
            System.out.println("numero= "+contador); 
        }


        int l = 0;

        int c;

        System.out.println("cuenta decreciente");

        for(c=50; c > l; c--)
        {
            System.out.println("numero= "+c); 
        }
    }
}
